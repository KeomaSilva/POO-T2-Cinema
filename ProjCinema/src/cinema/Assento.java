package cinema;

public class Assento {
	private String fileira;
	private int numero;
	
	public Assento() {
		
	}

	public String getFileira() {
		return fileira;
	}

	public void setFileira(String fileira) {
		this.fileira = fileira;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	

}
